package model;

public class Report {
	
	private int totalStudents;
	private int totalSkills;
	private int totalAssignedSkills;
	private int totalCategories;
	private double averageProgress;
	
	public int getTotalStudents() {
		return totalStudents;	
	}
	
	public void setTotalStudents(int totalStudents) {
		this.totalStudents = totalStudents;
	}
	
	public int getTotalSkills() {
		return totalSkills;	
	}
	
	public void setTotalSkills(int totalSkills) {
		this.totalSkills = totalSkills;
	}
	
	public int getTotalAssignedSkills() {
		return totalAssignedSkills;
	}
	
	public void setTotalAssignedSkills(int totalAssignedSkills) {
		this.totalAssignedSkills = totalAssignedSkills;
	}
	
	public int getTotalCategories() {
		return totalCategories;
	}
	
	public void setTotalCategories(int totalCategories) {
		this.totalCategories = totalCategories;
	}
	
	public double getAverageProgress() {
		return averageProgress;	
	}
	
	public void setAverageProgress(double averageProgress) {
		this.averageProgress = averageProgress;
	}
	
	

}
